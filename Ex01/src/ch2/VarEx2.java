package ch2;

class VarEx2 {
	
	public static void main(String[] args) {
		
		int x = 10;
		int y = 20;
		int temp = 0;
		
		System.out.println("x : "+x);
		System.out.println("y : "+y);
		System.out.println("temp : "+temp);
		
		temp = x;
		x = y;
				
		System.out.println("x : "+x);
		System.out.println("y : "+y);

		
	}

}
